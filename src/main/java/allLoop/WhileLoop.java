package allLoop;

public class WhileLoop {

	public static void main(String[] args) {
		int i=1;
		while(i<=10) {
			System.out.println(i);
			i=i+1;
		}
		System.out.println("Hello While Loop world");

		int j=1;
		while (j<=30) {
			System.out.println(j);
			j=j+2;
		}
		System.out.println("Hello While Loop printing odd number until 30");

		int k=2;
		while (k<=50) {
			System.out.println(k); 
			k=k+2;
		}
		System.out.println("Hello While Loop printing even number until 50"); 
		


	}

}
